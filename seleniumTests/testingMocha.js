var assert = require('assert');
describe('Math', function () {
    describe('#indexOf()', function () {
        it('should return 9 as answer of 3*3', function () {
            assert.equal(9, 3 * 3);
        });
        it('should return -8 as answer of (3 – 4)*8', function () {
            assert.equal(-8, (3 - 4) * 8);
        });
    });
});